def _apply_unified_diff(original: str, diff: str, target_file: str) -> str:
    """Apply a unified diff to original file content and return the new content."""
    lines = original.splitlines(True)
    diff_lines = diff.splitlines()

    i = 0
    while i < len(diff_lines):
        dl = diff_lines[i]

        hunk_match = re.match(r"^@@ -(\d+)(?:,(\d+))? \+(\d+)(?:,(\d+))? @@", dl)
        if not hunk_match:
            i += 1
            continue

        old_start = int(hunk_match.group(1))
        old_count = int(hunk_match.group(2) or 1)
        new_start = int(hunk_match.group(3))
        new_count = int(hunk_match.group(4) or 1)

        i += 1

        removed = []
        added = []
        while (
            i < len(diff_lines)
            and not diff_lines[i].startswith("@@")
            and not diff_lines[i].startswith("diff --git")
            and not diff_lines[i].startswith("--- ")
            and not diff_lines[i].startswith("+++ ")
        ):
            dl2 = diff_lines[i]
            if dl2.startswith("-"):
                removed.append(dl2[1:])
            elif dl2.startswith("+"):
                added.append(dl2[1:])
            elif dl2.startswith(" "):
                removed.append(dl2[1:])
                added.append(dl2[1:])
            i += 1

        idx = old_start - 1
        end_idx = min(idx + len(removed), len(lines))
        for j in range(idx, end_idx):
            stripped = lines[j].rstrip("\n").rstrip("\r")
            r_stripped = removed[j - idx].rstrip("\n").rstrip("\r")
            if stripped != r_stripped and removed[j - idx] != "":
                pass

        lines[idx:end_idx] = [(a if a.endswith("\n") else a + "\n") for a in added]

    return "".join(lines)


class PROpenerAgent(BaseAgent):
    def __init__(self):
        super().__init__(
            name="pr_opener",
            system_prompt="""You are the PR Opener Agent in SwarmOps.

Your job:
1. Create a feature branch from main/master
2. Apply the code changes
3. Commit with a conventional commit message
4. Open a pull request with detailed description
5. Include test results and security audit in PR body

Rules:
- Use conventional commit format: "fix: description" or "feat: description"
- PR description must include summary, test results, security audit, issue link
- Branch name format: "fix/issue-{number}-description" or "feat/issue-{number}"
- Do NOT merge the PR automatically

Output ONLY valid JSON with no markdown formatting:
{
    "pr_url": "https://github.com/owner/repo/pull/143",
    "pr_number": 143,
    "branch": "fix/issue-42-fix",
    "commit_sha": "abc123",
    "title": "fix: description",
    "description": "Full PR description with sections"
}""",
        )

    async def think(self, context: Dict[str, Any]) -> AgentMessage:
        self.set_status(AgentStatus.THINKING, "Creating pull request")

        issue = context.get("issue", {})
        repo = context.get("repo", {})
        code_writer = context.get("code_writer_output", {})
        test_runner = context.get("test_runner_output", {})
        security = context.get("security_auditor_output", {})

        issue_number = issue.get("number", 0)
        issue_title = issue.get("title", "Fix")
        repo_name = repo.get("name", "owner/repo")

        diff = code_writer.get("diff", "")
        files_changed = code_writer.get("files_changed", [])
        code_summary = code_writer.get("summary", "")

        # Try to actually create a real PR using GitHubService
        try:
            github = get_github_service()
            branch_name = f"fix/issue-{issue_number}-swarmops"
            default_branch = repo.get("default_branch", "main")

            pr_title = f"fix: {issue_title[:72].lower()}"
            pr_description = (
                f"## Summary\n"
                f"Automated fix for issue #{issue_number}: {issue_title}\n\n"
                f"## Changes\n{code_summary}\n\n"
                f"## Files Modified\n"
            )
            for f in files_changed:
                pr_description += f"- {f}\n"
            pr_description += (
                f"\n## Test Results\n"
                f"[OK] {test_runner.get('tests_passed', 0)}/{test_runner.get('total_tests', 0)} tests passed\n\n"
                f"## Security Audit\n"
                f"{'[OK] Passed' if security.get('passed', False) else '[FAIL] Failed'}\n\n"
            )
            for finding in security.get("findings", []):
                pr_description += f"- {finding.get('issue', '')}\n"
            pr_description += (
                f"\n## Original Issue\n{issue.get('url', 'N/A')}\n\n---\n*Generated by SwarmOps*"
            )

            # Create branch
            try:
                github.create_branch(repo_name, branch_name, default_branch)
            except Exception as branch_err:
                # Branch may already exist
                print(f"[WARN] Branch create (may exist): {branch_err}")

            # Commit each changed file
            commit_sha = None
            for file_path in files_changed:
                try:
                    original = github.get_file_content(repo_name, file_path, default_branch)
                    new_content = _apply_unified_diff(original, diff, file_path)
                    commit_url = github.commit_file(
                        repo_name,
                        file_path,
                        new_content,
                        f"fix: {issue_title[:50].lower()}",
                        branch_name,
                    )
                    print(f"[OK] Committed {file_path}: {commit_url}")
                except Exception as file_err:
                    print(f"[WARN] Could not commit {file_path}: {file_err}")

            # Create the PR
            pr_url = github.create_pull_request(
                repo_name, pr_title, pr_description, branch_name, base_branch=default_branch
            )

            result = {
                "pr_url": pr_url,
                "pr_number": issue_number,
                "branch": branch_name,
                "commit_sha": commit_sha or "applied",
                "title": pr_title,
                "description": pr_description,
            }
            confidence = 0.95

            self.set_status(AgentStatus.COMPLETED)
            self.confidence = confidence
            self.output = result

            return self.create_message(
                content=f"[OK] Pull Request #{result['pr_number']} opened: {result['title']}\n"
                f"[PR] {result['pr_url']}\n"
                f"[Branch] {result['branch']}",
                message_type="pr",
                data=result,
            )

        except Exception as e:
            print(f"[WARN] Real PR creation failed: {e}. Falling back to PR description only.")
            # Fallback: generate PR description but don't actually commit code
            branch_name = f"fix/issue-{issue_number}-swarmops"
            pr_title = f"fix: {issue_title[:50].lower()}"

            description_parts = [
                f"## Summary",
                f"Automated fix for issue #{issue_number}: {issue_title}",
                "",
                f"## Changes",
                code_summary,
                "",
                f"## Files Modified",
            ]
            for f in files_changed:
                description_parts.append(f"- {f}")
            description_parts += [
                "",
                f"## Test Results",
                f"[OK] {test_runner.get('tests_passed', 0)}/{test_runner.get('total_tests', 0)} tests passed",
                "",
                f"## Security Audit",
                "[OK] Passed" if security.get("passed", False) else "[FAIL] Failed",
            ]
            for finding in security.get("findings", []):
                description_parts.append(f"- {finding.get('issue', '')}")
            description_parts += [
                "",
                f"## Original Issue",
                issue.get("url", "N/A"),
                "",
                "---",
                "*Generated by SwarmOps*",
            ]

            result = {
                "pr_url": f"https://github.com/{repo_name}/pull/{issue_number}",
                "pr_number": issue_number,
                "branch": branch_name,
                "commit_sha": "uncommitted",
                "title": pr_title,
                "description": "\n".join(description_parts),
            }
            confidence = 0.85

            self.set_status(AgentStatus.COMPLETED)
            self.confidence = confidence
            self.output = result

            return self.create_message(
                content=f"[OK] Pull Request #{result['pr_number']} prepared (description only): {result['title']}\n"
                f"[PR] {result['pr_url']}\n"
                f"[Branch] {result['branch']}",
                message_type="pr",
                data=result,
            )

    def validate_output(self, output: Dict[str, Any]) -> tuple[bool, str]:
        if not output.get("pr_url"):
            return False, "Missing PR URL"
        if not output.get("pr_number"):
            return False, "Missing PR number"
        if not output.get("branch"):
            return False, "Missing branch name"
        if not output.get("title"):
            return False, "Missing PR title"
        return True, ""
